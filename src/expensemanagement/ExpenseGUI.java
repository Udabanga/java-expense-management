    public ArrayList<Expense> expenseList() {
        ArrayList<Expense> expenseList = new ArrayList<>();
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/pe", "root", "");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM expense");
            Expense expense;
            while (rs.next()) {
                expense = new Expense(rs.getInt("ID"), rs.getString("tDetails"), rs.getDate("Date"), rs.getDouble("Amount"));
                expenseList.add(expense);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        return expenseList;
    }

    public void showTable() {
        ArrayList<Expense> list = expenseList();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        Object[] row = new Object[4];
        double total = 0;
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getID();
            row[1] = list.get(i).gettDetails();
            row[2] = list.get(i).getDate();
            row[3] = list.get(i).getAmount();
            model.addRow(row);
            total = total + list.get(i).getAmount();
        }
        totalTextField.setText(Double.toString(total));
    }

