    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        if (jTable1.getSelectedRowCount() == 1) {
            int row = jTable1.getSelectedRow();
            try {
                Connection con = DriverManager.getConnection(
                        "jdbc:mysql://localhost:3306/pe", "root", "");
                PreparedStatement pst = con.prepareStatement("DELETE FROM expense WHERE ID=?");
                pst.setString(1, jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
//            pst.setString(1, (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
                pst.executeUpdate();
                model.removeRow(row);
                showTable();
            } catch (Exception e) {
                System.out.println(e);
            }
        } else {
            if (jTable1.getSelectedRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Table Empty");
            } else {
                JOptionPane.showMessageDialog(this, "Select Row to Delete");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

